const normalizeIconName = (iconName) => iconName
  .trim()
  .replace(/^mdi-/, '')
  .replace(/^llars:/, '')
  .replace(/^llars-/, '')

const tokenize = (iconName) => normalizeIconName(iconName)
  .replace(/-outline|-variant/g, '')
  .split(/[-_]/)
  .filter(Boolean)

const hasAny = (tokens, values) => values.some((value) => tokens.includes(value))

export const resolveIconKey = (iconName) => {
  if (!iconName || typeof iconName !== 'string') return null
  const raw = iconName.trim()

  if (iconComponents[raw]) return raw
  if (explicitAliases[raw]) return explicitAliases[raw]

  const normalized = normalizeIconName(raw)
  if (iconComponents[normalized]) return normalized

  const tokens = tokenize(raw)

  if (tokens.includes('alpha') && tokens.includes('a')) return 'alpha-a'
  if (tokens.includes('alpha') && tokens.includes('b')) return 'alpha-b'

  if (hasAny(tokens, ['cog', 'gear', 'settings', 'tools', 'tool', 'wrench'])) return 'gear'
  if (hasAny(tokens, ['history'])) return 'history-circle'
  if (hasAny(tokens, ['clock', 'time', 'timer', 'progress'])) return 'clock'
  if (hasAny(tokens, ['chart', 'graph', 'analytics', 'stats', 'metric', 'timeline', 'speed', 'speedometer', 'percent', 'pillar', 'counter'])) return 'chart-bar'
  if (hasAny(tokens, ['copy', 'paste', 'duplicate'])) return 'copy'
  if (hasAny(tokens, ['rocket', 'launch', 'fire'])) return 'bolt'
  if (hasAny(tokens, ['target', 'bullseye', 'goal'])) return 'trophy'

  if (hasAny(tokens, ['account', 'user', 'person', 'profile', 'human'])) {
    if (hasAny(tokens, ['plus', 'add', 'new'])) return 'user-plus'
    if (hasAny(tokens, ['check', 'verified'])) return 'user-check'
    if (hasAny(tokens, ['group', 'multiple', 'users', 'team'])) return 'users-group'
    return 'users'
  }

  if (tokens.includes('shield') || tokens.includes('sword')) return 'shield-check'
  if (tokens.includes('lock') || tokens.includes('key') || tokens.includes('gate')) return 'lock'

  if (hasAny(tokens, ['trash', 'delete', 'remove'])) return 'trash'
  if (hasAny(tokens, ['close', 'cancel', 'x'])) return 'x'

  if (hasAny(tokens, ['plus', 'add', 'new'])) return 'plus'
  if (tokens.includes('minus')) return 'minus'

  if (tokens.includes('checkbox')) {
    if (tokens.includes('check') || tokens.includes('on')) return 'check'
    if (tokens.includes('minus') || tokens.includes('indeterminate')) return 'minus'
    return 'dot'
  }

  if (tokens.includes('check') || tokens.includes('success')) {
    if (tokens.includes('circle') || tokens.includes('decagram')) return 'check-circle'
    return 'check'
  }

  if (hasAny(tokens, ['alert', 'warning', 'error', 'danger', 'thermometer'])) return 'triangle-alert'
  if (hasAny(tokens, ['info', 'help', 'question', 'information'])) return 'info-circle'

  if (hasAny(tokens, ['eye', 'view'])) {
    if (tokens.includes('off') || tokens.includes('hide')) return 'eye-off'
    return 'eye'
  }
  if (hasAny(tokens, ['incognito', 'private', 'hidden'])) return 'eye-off'

  if (hasAny(tokens, ['refresh', 'sync', 'reload', 'repeat', 'restart', 'restore', 'infinity'])) return 'refresh'
  if (hasAny(tokens, ['loading', 'spin'])) return 'spinner'

  if (hasAny(tokens, ['search', 'magnify', 'find'])) return 'search'
  if (hasAny(tokens, ['menu', 'dots'])) return 'menu'

  if (tokens.includes('upload')) return 'upload'
  if (tokens.includes('download') || tokens.includes('save')) return 'download'

  if (tokens.includes('folder')) {
    if (hasAny(tokens, ['plus', 'add', 'new', 'create'])) return 'folder-plus'
    return 'folder'
  }
  if (hasAny(tokens, ['file', 'document', 'note', 'paper', 'pdf', 'word', 'excel', 'powerpoint', 'license', 'archive'])) {
    if (hasAny(tokens, ['plus', 'add', 'new', 'create'])) return 'file-plus'
    return 'file'
  }

  if (tokens.includes('clipboard')) return 'clipboard'
  if (tokens.includes('filter') || tokens.includes('tune')) return 'filter'
  if (hasAny(tokens, ['cloud', 'wifi', 'ethernet', 'connection', 'network'])) return 'cloud'

  if (hasAny(tokens, ['chat', 'message', 'comment', 'bubble', 'thought', 'emoticon', 'face', 'agent'])) return 'chat'
  if (tokens.includes('bell')) return 'bell'
  if (tokens.includes('calendar')) return 'calendar'

  if (hasAny(tokens, ['tag', 'label', 'identifier'])) return 'tag'
  if (hasAny(tokens, ['pin', 'bookmark', 'flag'])) return 'pin'

  if (hasAny(tokens, ['grid', 'apps', 'dashboard', 'table', 'view', 'matrix', 'puzzle', 'cube', 'sitemap', 'card'])) return 'grid'
  if (hasAny(tokens, ['list', 'playlist', 'menu'])) return 'unordered-list'

  if (hasAny(tokens, ['code', 'braces', 'json', 'console', 'api', 'function', 'sigma', 'division', 'calculator', 'numeric', 'source', 'gitlab', 'docker', 'vector', 'polygon'])) return 'code'
  if (hasAny(tokens, ['text', 'format', 'bold', 'italic', 'underline', 'header', 'quote', 'title', 'pilcrow', 'markdown', 'language', 'keyboard'])) return 'text'

  if (hasAny(tokens, ['image', 'photo', 'camera'])) return 'image'
  if (hasAny(tokens, ['link', 'chain', 'paperclip', 'attachment'])) return 'link'

  if (hasAny(tokens, ['email', 'mail'])) return 'mail'
  if (hasAny(tokens, ['book', 'library', 'bookshelf'])) return 'book'

  if (hasAny(tokens, ['robot', 'ai', 'brain', 'openai'])) return 'robot'
  if (tokens.includes('flask')) return 'flask'

  if (hasAny(tokens, ['gavel', 'scale', 'balance'])) return 'scale'
  if (tokens.includes('school')) return 'school'

  if (tokens.includes('lightbulb') || tokens.includes('idea')) return 'lightbulb'
  if (hasAny(tokens, ['bolt', 'lightning'])) return 'bolt'

  if (hasAny(tokens, ['palette', 'color'])) return 'palette'
  if (hasAny(tokens, ['sun', 'sunny', 'light', 'day', 'brightness'])) return 'sun'
  if (hasAny(tokens, ['moon', 'night', 'dark'])) return 'moon'
  if (hasAny(tokens, ['theme', 'auto', 'system'])) return 'system-theme'

  if (tokens.includes('fullscreen')) return 'fullscreen'

  if (tokens.includes('pause') || tokens.includes('sleep')) return 'pause'
  if (tokens.includes('stop')) return 'stop'
  if (tokens.includes('play') || tokens.includes('skip')) return 'play'

  if (hasAny(tokens, ['share', 'send', 'publish', 'broadcast', 'transit'])) return 'share'
  if (hasAny(tokens, ['trophy', 'podium', 'crown'])) return 'trophy'
  if (tokens.includes('star')) return 'star'
  if (tokens.includes('theater')) return 'star'
  if (hasAny(tokens, ['pencil', 'edit', 'rename'])) return 'pencil'
  if (hasAny(tokens, ['magic', 'wizard', 'auto', 'wand', 'fix'])) return 'wand'

  if (hasAny(tokens, ['globe', 'earth', 'web', 'map', 'translate', 'google'])) return 'globe'

  if (hasAny(tokens, ['database', 'server', 'harddisk', 'memory', 'cpu', 'chip', 'storage', 'drive'])) return 'database'

  if (tokens.includes('heart')) return 'heart'
  if (tokens.includes('thumb')) {
    if (tokens.includes('down')) return 'thumb-down'
    return 'thumb-up'
  }

  if (hasAny(tokens, ['swap', 'compare', 'shuffle', 'undo', 'redo', 'mirror', 'drag'])) return 'swap'

  if (hasAny(tokens, ['arrow', 'chevron'])) {
    if (tokens.includes('left') && tokens.includes('right')) return 'swap'
    if (tokens.includes('up') && tokens.includes('down')) return 'swap'
    if (tokens.includes('left') || tokens.includes('back')) return 'arrow-left'
    if (tokens.includes('right') || tokens.includes('forward')) return 'arrow-right'
    if (tokens.includes('up')) return 'arrow-up'
    if (tokens.includes('down')) return 'arrow-down'
    return 'arrow-right'
  }

  if (hasAny(tokens, ['sort'])) return 'arrow-down'

  return 'question'
}

export const resolveIconComponent = (iconName) => {
  if (!iconName) return null
  if (typeof iconName !== 'string') return iconName
  const key = resolveIconKey(iconName)
  return iconComponents[key] || QuestionIcon
}

